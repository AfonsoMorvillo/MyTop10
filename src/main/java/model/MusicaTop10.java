package model;

public class MusicaTop10 {
	private int id;
	private Musica musica;
	private Top10 top10;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Musica getMusica() {
		return musica;
	}
	public void setMusica(Musica musica) {
		this.musica = musica;
	}
	public Top10 getTop10() {
		return top10;
	}
	public void setTop10(Top10 top10) {
		this.top10 = top10;
	}
	
}
